package business;
public abstract class Facility {

	int id;
	String name;
	String address;
	String city;
	String state;
	int zip;
	
	public Facility() {};
	
	public int getId() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	
	public String getAddress() {
		return address;
	}
	
	public String getCity() {
		return city;
	}
	
	public String getState() {
		return state;
	}

	public int getZip() {
		return zip;
	}

	
	
	
	
	
}
